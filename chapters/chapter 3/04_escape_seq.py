# 1  = \n   new line ke liye use karte hai
a = "Hariom is a good boy \n but not a bad boy"
print(a)

# output
# Hariom is a good boy 
#  but not a bad boy

# 2  = \t new tab ke liye use karte hai
b = "Hariom is a good boy\tbut not a bas boy"
print(b)

# output
# Hariom is a good boy    but not a bas boy


# 3  = \' jab hame puri string me kisi words ko sigle qouate me likhna ho 

c = "Hariom is a good boy \'but\'not a bad boy"
print(c)

# output
# Hariom is a good boy 'but'not a bad boy


